export const arcanaBySlug = (slug: string) => ARCANA.find((a) => a.slug === slug);

export function pickRandomDistinct(n: number): Arcana[] {
  const pool = [...ARCANA];
  const out: Arcana[] = [];
  for (let i = 0; i < n; i++) {
    const idx = Math.floor(Math.random() * pool.length);
    out.push(pool.splice(idx, 1)[0]);
  }
  return out;
}
